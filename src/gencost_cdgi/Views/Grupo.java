package gencost_cdgi.Views;

/**
 *
 * @author danil
 */
public class Grupo {

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the imagem
     */
    public String getImagem() {
        return imagem;
    }

    /**
     * @param imagem the imagem to set
     */
    public void setImagem(String imagem) {
        this.imagem = imagem;
    }
    
    public static synchronized Grupo getInstance() {
        if (instancia == null) {
            instancia = new Grupo();
        }
        return instancia;
    }
        
    private int id;
    private String nome;
    private String imagem;
    private static Grupo instancia;
    private Grupo(){
        
    }
    
}
